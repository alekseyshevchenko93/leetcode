const DIRECTIONS = [
  [0,-1],
  [0,1],
  [-1,0],
  [1,0]
]

function bfs(grid, i, j, rows, cols, visited) {
  const q = [[i, j]];
  const key = `${i}_${j}`;
  visited.add(key);
  while (q.length) {
    const [r, c] = q.shift();

    DIRECTIONS.forEach((direction) => {
      const xDirection = r + direction[0];
      const yDirection = c + direction[1];
      const key = `${xDirection}_${yDirection}`;

      if (xDirection < 0 || xDirection >= rows || yDirection < 0 || yDirection >= cols) {
        return;
      }

      const item = grid[xDirection][yDirection];

      if (item === '1' && !visited.has(key) ) {
        visited.add(key);
        q.push([xDirection, yDirection]);
      }
    });
  }
}

